     private void listarProdutosVendidos(){
        ProdutosDAO produtosDao = new ProdutosDAO(); // Buscar os dados
            ArrayList<ProdutosDTO> listaProdutos = produtosDao.getProdutosVendidos(); // Recuperar os dados

            DefaultTableModel tabelaProdutos = (DefaultTableModel) tblVendidos.getModel();
            tabelaProdutos.setRowCount(0);

            // Ordenador de linhas Ã  tabela
            tblVendidos.setRowSorter(new TableRowSorter<>(tabelaProdutos));

            // Preenche a tabela com os dados do banco de dados
            for (ProdutosDTO c : listaProdutos) {
                Object[] obj = new Object[]{
                    c.getId(),   // Id
                    c.getNome(),   // Nome do produto
                    c.getValor(),    // Valor
                    c.getStatus() // Status
                };
                tabelaProdutos.addRow(obj);
            }
    
    }


